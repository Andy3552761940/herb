// 根据ID获取药材
export const getHerbById = (id: string): Herb | undefined => {
  return HERBS_DATA.find(herb => herb.id === id);
};

// 根据区域获取药材
export const getHerbsByRegion = (region: string): Herb[] => {
  return HERBS_DATA.filter(herb => herb.region === region);
};

// 根据稀有度获取药材
export const getHerbsByRarity = (rarity: string): Herb[] => {
  return HERBS_DATA.filter(herb => herb.rarity === rarity);
};

// 获取随机药材
export const getRandomHerbs = (count: number): Herb[] => {
  const shuffled = [...HERBS_DATA].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};
